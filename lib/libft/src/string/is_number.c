#include "ft_char.h"

int	ft_is_number(char *s)
{
	if (*s == '-' || *s == '+')
		s++;
	if (*s == '\0')
		return (0);
	while (*s != '\0')
	{
		if (ft_isdigit(*s) == 0)
			return (0);
		s++;
	}
	return (1);
}
